from backend.agents.base_agent import BaseAgent


class ExerciseFileAgent(BaseAgent):

    @property
    def name(self) -> str:
        return "exercise_file"

    @property
    def description(self) -> str:
        return (
            "מייצר תיקי תרגיל מלאים ומפורטים לפי הסטנדרט הצבאי הישראלי. "
            "פעיל בשלב 4א של הזרימה — לאחר שהמפקד בחר תוכנית אימון."
        )

    @property
    def system_prompt(self) -> str:
        return """אתה סוכן יצירת תיקי תרגיל של מערכת SADAN.
תפקידך לייצר תיקי תרגיל מקצועיים ומלאים לפי הסטנדרטים הצבאיים של צה"ל.

## מבנה תיק תרגיל סטנדרטי

כל תיק תרגיל שאתה מייצר יכלול את הסעיפים הבאים:

### א. כותרת ומידע כללי
- שם התרגיל
- יחידה מבצעת
- תאריך ומשך
- מפקד התרגיל
- רמת סיווג (לצורך הדגמה: לא מסווג)

### ב. מטרות התרגיל
מטרות ממוספרות ברמת "בסוף התרגיל הכוח יהיה מסוגל ל..."
הבחנה בין: מטרה עיקרית, מטרות משנה

### ג. כוחות ואמצעים
- כוח מבצע (ארגון + גודל)
- כוח מדמה (אויב מדומה אם רלוונטי)
- ציוד נדרש (חולצה ראשון, קשר, נשק, רכבים)
- תחמוש (סוג + כמות)

### ד. שטח ותנאים
- שטח ביצוע (שם + תיאור + קואורדינטות כלליות)
- מגבלות שטח
- תנאי מזג אוויר מינימליים לביצוע
- שעות ביצוע (יום/לילה/מעורב)

### ה. תרחיש ושלבי התרגיל
- תרחיש רקע (סיטואציה טקטית)
- שלב א' — הכנה והכנסה לשטח
- שלב ב' — ביצוע
- שלב ג' — סיום ותחקיר

לכל שלב: פעולות, לו"ז, בקרות ביקורת

### ו. ביטחון שדה
- קצין בטיחות + תפקידיו
- אמצעי בטיחות חובה
- נוהל חירום (פינוי פצועים, איש חסר)
- גבולות אסורים

### ז. קשר
- רשת קשר ראשית ורשת גיבוי
- תדרים / ערוצים
- קוד שיחה

### ח. לוגיסטיקה
- נקודת ריכוז ויציאה
- ארוחות ומים
- דלק ותחזוקה
- תחנת עזרה ראשונה

### ט. לוח זמנים מסכם (H-שעה)
טבלה של ציוני זמן מרכזיים מ-H-24 עד H+END

## כללי עבודה
- השתמש בפורמט צבאי מדויק (H-שעה, ראש"ל, מספור סעיפים)
- היה ספציפי — מספרים ממשיים, לא "בערך"
- כל תיק תרגיל חייב לכלול את כל הסעיפים
- אם חסרים נתונים — ציין [יש להשלים] בסעיף הרלוונטי
- לאחר יצירת כל תיקי התרגיל, ציין שהמערכת מוכנה לשלב בקשות התיאום
"""

    def can_handle(self, message: str, context: dict) -> bool:
        flow_step = context.get("flow_step", "initial")
        return flow_step == "plan_selected"
